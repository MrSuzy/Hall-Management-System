    public void populateEditFields(String hallID) {
    hallClass hall = new hallClass();
    Object[] hallDetails = hall.getSpecificHallDetails(hallID); // Call the method to get details

    // Check if hallDetails is not null (i.e., hall found)
    if (hallDetails != null) {
        txtEditHallID.setText(hallDetails[0].toString()); // Set Hall ID
        txtEditHallType.setText(hallDetails[1].toString()); // Set Hall Type
        txtEditCapacity.setText(hallDetails[3].toString()); // Set Capacity
        txtEditPrice.setText(hallDetails[2].toString()); // Set Price
    } else {
        JOptionPane.showMessageDialog(this, "Hall not found.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}
