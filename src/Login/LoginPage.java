    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String email = txtEmail.getText();
        String password = new String(pwdPass.getPassword());
        
        try{
            Object user = authenticate(email, password);
            
            if (user != null) {
                loggedInEmail = email;
                String role = ""; 
                
               // determine role of user
               if (user instanceof customerClass) {
                   role = "customer";
               } else if (user instanceof adminClass1) {
                   role = "admin";
               } else if (user instanceof managerLoginClass) {
                   role = "manager";
               } else if (user instanceof schedulerLoginClass) {
                   role = "scheduler";
               }
               
               JOptionPane.showMessageDialog(null, "Login Successful");
               dispose();
               
               // switch case to navigate to respective main page
               switch(role.toLowerCase()) {
                   case "customer":
                       new customerMainMenu(loggedInEmail).setVisible(true);
                       break;
                   case "admin":
                       new adminMainPage(loggedInEmail).setVisible(true);
                       break;
                   case "manager":
                       new managerMainMenu(loggedInEmail).setVisible(true);
                       break;
                   case "scheduler":
                       new schedulerMainMenu(loggedInEmail).setVisible(true);
                       break;
                   default:
                       JOptionPane.showMessageDialog(null, "Invalid role.");
                       break;
               }
            } else {
                JOptionPane.showMessageDialog(null, "Invalid email or password");
            }
            
        } catch (IOException e) {
            e.printStackTrace();
        }

        
        

    }//GEN-LAST:event_btnLoginActionPerformed

